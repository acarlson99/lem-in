#include "libft.h"

void	ft_numunpair(uint64_t p, uint32_t *x, uint32_t *y)
{
	int		i;

	i = 0;
	*x = 0;
	*y = 0;
	while (p)
	{
		*x = *x | ((uint32_t)(p & 1) << i);
		p >>= 1;
		*y = *y | ((uint32_t)(p & 1) << i);
		p >>= 1;
		i++;
	}
}
