#include "libft.h"

int		ft_putnstr_fd(char *s, size_t n, int fd)
{
	size_t		len;

	len = 0;
	RET_IF(!(s), 0);
	while (*s && n--)
	{
		write(fd, s, 1);
		s++;
		len++;
	}
	return (len);
}
