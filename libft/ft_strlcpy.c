#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t len)
{
	int i;

	i = 0;
	RET_IF(!(dst && src), 0);
	while (src[i] && len-- > 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	while (src[i])
		i++;
	return (i);
}
