#include "libft.h"

size_t	ft_wordnum(const char *s, char c)
{
	size_t w;

	w = 0;
	while (*s)
	{
		if (*s == c)
			s++;
		if (*s && *s != c)
			w++;
		while (*s && *s != c)
			s++;
	}
	return (w);
}
